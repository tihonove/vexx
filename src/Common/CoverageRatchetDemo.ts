// эти строки должны уронить покрытие ниже порога и сделать PR красным,
// а отчёт в комментарии — показать их как непокрытые. Удалить после проверки.

export function classifyNumber(n: number): "negative" | "zero" | "positive" {
    if (n < 0) {
        return "negative";
    }
    if (n === 0) {
        return "zero";
    }
    return "positive";
}
